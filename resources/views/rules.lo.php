    <h3>Registrací do systému souhlasíte s těmito pravidly:</h3>
    
    1. Vaše jméno by nemělo obsahovat speciální znaky (např. \_ nebo $)
    2. Vaše jméno by dále nemělo obsahovat mezery
    3. Email při registraci musí být platný (Pokud není, nelze později obnovit zapomenuté heslo atd.)
    4. Heslo by mělo obsahovat min. 8 znaků, 1 velké písmeno a 1 číslo
    5. Heslo zadejte takové, které nikde jinde nemáte zaregistrované!
    6. Mějte na paměti, že špatně zadané údaje ovlivní komfort Vašeho procházení webu nebo přihlášení

    Registrací do systému souhlasíte dále s těmito pravidly:

    1. Jeden uživatel má pouze jeden profilový účet
    2. Uživatel je povinen chovat se dle dobrých společenských mravů a konvencí
    3. Na webu jsou zakázány veškeré urážky nebo chování odporujícím dobrým mravům a zákonům České Republiky
    4. Je zakázána reklama nebo zveřejňování IP adres jiných serverů
    5. Příspěvky nepsat pouze velkým písmem (např.: se zapnutým CAPSLOCKem)
    6. Příspěvky a komentáře by měli být v souladu s českým pravopisem
    7. Pokud je během používání webu nalezena chyba, je uživatel povinen na chybu upozornit administrátora webu
    8. Nezatěžovat web rychle jdoucími po sobě opakovanými funkcemi
    9. Nevkládat obrázky odporující pravidlům webu
    10. Zaregistrovaný uživatel by měl navštívit web min. 1 za půl roku. Po půl roce neaktivity bude uživatel nenávratně smazán

    Co vše webová stránka DXGamePRO shromažďuje za údaje?

    1. Základní údaje: jméno, heslo-otisk, email, role (pokud jí uživatel dostane)
    2. Systém dále vygeneruje tyto údaje: datum první registrace, datum posledního přihlášení
    3. Pro funkci automatického přihlášení uživatele web shromažďuje tzn.: COOKIES (nick, otisk hesla, webový styl, uživatelské ID)
    4. Uživatelský status (online, offline atd.), celkový počet napsaných příspěvků, komentářů atd.
    5. Napsané příspěvky, vytvořená témata
    6. Počet hodnocených příspěvků
    7. Systém LOGuje shora uvedenou aktivitu uživatele mimo textů příspěvků


    Jaká práva má administrátor webové stránky?

    Pokud administrátor dostane podnět od ostatních uživatelů, že daný uživatel porušuje pravidla nebo povinnosti shora uvedené, je admin oprávněn:

    1. Zkontrolovat LOG systému
    2. Zkontrolovat veškeré soukromé zprávy daného uživatele
    3. Zkontrolovat vložené obrázky, zdali odpovídají pravidlům webu
    4. Upozornit uživatele na porušování pravidel nebo povinností webu
    5. Pokud daný uživatel bude i po upozornění administrátorem dále porušovat pravidla a povinnosti webu, může být smazán nebo zabanován
